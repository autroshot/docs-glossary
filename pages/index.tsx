import { ExternalLinkIcon, MoonIcon } from '@chakra-ui/icons';
import {
  Box,
  Container,
  Flex,
  Heading,
  HStack,
  IconButton,
  Link,
  Spacer,
} from '@chakra-ui/react';
import { InferGetStaticPropsType } from 'next';
import Head from 'next/head';
import GlossaryTable, { Term } from '../components/glossaryTable';
import { readFile } from 'fs/promises';
import Papa from 'papaparse';
import path from 'path';

export default function Home({
  terms,
}: InferGetStaticPropsType<typeof getStaticProps>) {
  return (
    <>
      <Head>
        <title>용어집</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <nav>
        <Flex px={6} py={4} gap="5" align="center">
          <Box>용어집</Box>
          <Box>출처</Box>
          <Spacer />
          <IconButton aria-label="다크 모드" icon={<MoonIcon />} />
        </Flex>
      </nav>
      <main>
        <Container centerContent maxW="container.xl" my={3}>
          <Heading>용어집</Heading>
          <GlossaryTable terms={terms} />
        </Container>
      </main>
      <footer>
        <Container centerContent maxW="container.xl" my={3}>
          <HStack spacing="3rem">
            <Link
              href="https://autroshot.github.io/docs-repository/"
              isExternal
            >
              문서 저장소 <ExternalLinkIcon verticalAlign="0" />
            </Link>
            <Link href="#" isExternal>
              GitHub <ExternalLinkIcon verticalAlign="0" />
            </Link>
          </HStack>
        </Container>
      </footer>
    </>
  );
}

export async function getStaticProps() {
  const terms: Term[] = [];

  const file = await readFile(
    path.join(process.cwd(), 'public', '/glossary.csv'),
    'utf-8'
  );

  Papa.parse<string[]>(file, {
    complete(results) {
      if (results.data.length !== 0) {
        results.data.map((term, index) => {
          if (index >= results.data.length - 1) return;
          terms.push({
            english: term[0] ?? '',
            korean: term[1] ?? '',
            comment: term[2] ?? '',
            type: (term[3] ?? '') as '일반' | '고유',
            field: term[4] ?? '',
          });
        });
      }
    },
  });

  return { props: { terms } };
}
